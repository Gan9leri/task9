package JsonModels;

public class Capacity {

    Integer countFreezer;
    Integer countShelf;
    Integer countDoor;

    public Integer getCountFreezer() {
        return countFreezer;
    }

    public void setCountFreezer(Integer countFreezer) {
        this.countFreezer = countFreezer;
    }

    public Integer getCountShelf() {
        return countShelf;
    }

    public void setCountShelf(Integer countShelf) {
        this.countShelf = countShelf;
    }

    public Integer getCountDoor() {
        return countDoor;
    }

    public void setCountDoor(Integer countDoor) {
        this.countDoor = countDoor;
    }
}